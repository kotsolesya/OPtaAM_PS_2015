public class MassClass {
    public static final int k = 12;

    public static int add(int x) {
        return x + k;
    }

    public static int subtract(int x) {
        return x - k;
    }

    public static int multiply(int x) {
        return x * k;
    }

    public static int incrment(int x) {
        return ++x;
    }

    public static int decrement(int x) {
        return --x;
    }
}
